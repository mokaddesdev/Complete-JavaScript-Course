/**
 * Logical Operator
 *
 */
// ! Logical &&

console.log( true && true );
console.log( true && false );
console.log( false && true );
console.log( false && false );

console.log(  0 && 0 );
console.log( 0 && 1 );
console.log( 1 && 0 );
console.log( 1 && 1 );

//! Logical OR( || )

console.log( true || true );
console.log( true || false );
console.log( false || true );
console.log( false || false );

console.log( 1 || 1 );
console.log( 1 || 0 );
console.log( 0 || 1 );
console.log( 0 || 0 );

//! Logical Not ( ! )

console.log( ! true );
console.log( ! false );

console.log( 4 > 3 && 5 > 7 );
console.log( 8 > 10 && 12 < 20 );
console.log( 8 < 10 && 12 > 20 );
console.log( 8 < 10 && 12 < 20 );


console.log( 4 > 3 || 5 > 7 );
console.log( 8 > 10 || 12 < 20 );
console.log( 8 < 10 || 12 > 20 );
console.log( 8 < 10 || 12 < 20 );
