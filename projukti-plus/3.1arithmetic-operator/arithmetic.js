let numbOne = 12;
let numberTwo = 3;

let add = numbOne + numberTwo;
console.log( add );

let sub = numbOne - numberTwo;
console.log( sub );

let multi = numbOne * numberTwo;
console.log( multi );

let div = numbOne / numberTwo;
console.log( div );

let module = numbOne % numberTwo;
console.log( module );

let power = numbOne ** numberTwo;
console.log( power );